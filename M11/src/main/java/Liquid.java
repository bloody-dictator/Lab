public class Liquid {
    public double densityLiquid;

    public Liquid(double densityLiquid){
        this.densityLiquid = densityLiquid;
    }

    public double getDensityLiquid() {
        return densityLiquid;
    }

    public void setDensityLiquid(double densityLiquid) {
        this.densityLiquid = densityLiquid;
    }
}
